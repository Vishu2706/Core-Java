
public class Datatype {

    public static void main(String[] args) {
        boolean isTrue = true;
        char singleChar = 'A';
        byte smallInteger = 100;
        short mediumInteger = 32767;
        int largeInteger = 2147483647;
        long veryLargeInteger = 9223372036854775807L;
        float decimalNumber = 3.14f;
        double largeDecimalNumber = 1.7976931348623157E308;

        System.out.println("Boolean: " + isTrue);
        System.out.println("Character: " + singleChar);
        System.out.println("Byte: " + smallInteger);
        System.out.println("Short: " + mediumInteger);
        System.out.println("Integer: " + largeInteger);
        System.out.println("Long: " + veryLargeInteger);
        System.out.println("Float: " + decimalNumber);
        System.out.println("Double: " + largeDecimalNumber);
    }
}
